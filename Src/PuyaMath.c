    Foc->slIbusMaxVal.sl = Foc->slIbusMaxVal.sl + ((SLONG)1000 * (Foc->IbusMaxVal - Foc->slIbusMaxVal.sw.hi));

//	Foc->slPowerPu.sl=Foc->slPowerPu.sl+((SLONG)100*(((SLONG)Foc->IbusAdc1*Foc->slVdcMean.sw.hi>>12)-Foc->slPowerPu.sw.hi));
