    man_swVb = ((-16384 * Foc->ValfaLpf) >> 15) + ((28378 * Foc->VbetaLpf) >> 15); //28378=2^15*sqrt(3)/2
    man_swVc = ((-16384 * Foc->ValfaLpf) >> 15) - ((28378 * Foc->VbetaLpf) >> 15);

