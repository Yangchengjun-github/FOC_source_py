    // InvPark: From Vd&Vq to calc Valfa&Vbeta
    Foc->ValfaLpf = (man_swVd * Foc->wCos >> 15) - (man_swVq * Foc->wSin >> 15); // Q30=Q15*Q15+Q15*Q15
